// Remove elements from an array
// Create an array with multiple elements.
// Use the pop() method to remove the last element from the array.
// Use the shift() method to remove the first element from the array.
// Log the array to the console to see the updated elements.

const drinks = ['Origin', 'Hero beer', 'Life beer', 'Hennessy', 'Henieken beer'] // Created an array with multiple elements.
drinks.pop() // Used the pop() method to remove the last element from the array.
console.log(drinks) // Output elements : [ 'Origin', 'Hero beer', 'Life beer', 'Hennessy' ]

drinks.shift() // Used the shift() method to remove the first element from the array.
console.log(drinks) // Output elements : [ 'Hero beer', 'Life beer', 'Hennessy' ]
